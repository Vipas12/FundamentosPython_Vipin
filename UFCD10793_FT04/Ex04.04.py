'''Escreve um programa para escrever no ecrã a palavra olá 100 vezes.'''
#adiciono um contador para guardar o número inicial
contador = 1
#enquanto o contador for menor ou igual a 100, escrevo a palavra olá e incremento o contador para aparecer a quantidade de vezes
while contador <= 100:
    #imprime os olás
    print(f"{contador} Olá")
     #aumenta o contador para avançar para o próximo olá
    contador += 1   
   
    
# x+=1 é o mesmo que x = x + 1
# x-=1 é o mesmo que x = x - 1
# x*=1 é o mesmo que x = x * 1
# x/=1 é o mesmo que x = x / 1
# x%=1 é o mesmo que x = x % 1



